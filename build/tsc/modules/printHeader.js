export var printHeader = function (container, title) {
    var divTitle = document.createElement('div');
    divTitle.setAttribute('class', 'col-9');
    var h1Txt = document.createElement('h1');
    var txtTitle = document.createTextNode(title);
    h1Txt.appendChild(txtTitle);
    divTitle.appendChild(h1Txt);
    var divBreadCrumb = document.createElement('div');
    divBreadCrumb.setAttribute('class', 'col-3');
    var nav = document.createElement('nav');
    nav.setAttribute('aria-label', 'breadcrumb');
    var ol = document.createElement('ol');
    ol.setAttribute('class', 'breadcrumb');
    var liTxt = document.createElement('li');
    liTxt.setAttribute('class', 'breadcrumb-item');
    var a = document.createElement('a');
    a.setAttribute('href', '#');
    var txtHome = document.createTextNode('Home');
    a.appendChild(txtHome);
    liTxt.appendChild(a);
    var li2Txt = document.createElement('li');
    li2Txt.setAttribute('class', 'breadcrumb-item active');
    li2Txt.setAttribute('aria-current', 'page');
    var txtLib = document.createTextNode('Library');
    li2Txt.appendChild(txtLib);
    ol.appendChild(liTxt);
    ol.appendChild(li2Txt);
    nav.appendChild(ol);
    divBreadCrumb.appendChild(nav);
    container === null || container === void 0 ? void 0 : container.appendChild(divTitle);
    container === null || container === void 0 ? void 0 : container.appendChild(divBreadCrumb);
};
