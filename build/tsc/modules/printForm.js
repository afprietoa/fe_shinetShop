export var printForm = function (container, array, title) {
    var form = document.createElement('form');
    var divTitle = document.createElement('div');
    divTitle.setAttribute('class', 'text-center mb-4');
    var h1Txt = document.createElement('h1');
    h1Txt.setAttribute('class', 'mb-3');
    var txtH1 = document.createTextNode(title);
    h1Txt.appendChild(txtH1);
    divTitle.appendChild(h1Txt);
    form.appendChild(divTitle);
    array.forEach(function (type) {
        var divInput = document.createElement('div');
        divInput.setAttribute('class', 'form-floating mb-3');
        var input = document.createElement('input');
        input.setAttribute('class', 'form-control');
        input.setAttribute('type', type);
        input.setAttribute('id', type);
        input.setAttribute('data-type', type);
        input.setAttribute('data-type', type);
        input.setAttribute('minlength', "3");
        input.setAttribute('maxlength', "30");
        input.setAttribute('pattern', "^[a-zA-Z ]*$");
        var label = document.createElement('label');
        label.setAttribute('for', type);
        var txtLabel = document.createTextNode(type);
        label.appendChild(txtLabel);
        var spanMsg = document.createElement('span');
        spanMsg.setAttribute('class', 'input-message-error');
        var txtMsg = document.createTextNode("his field is not valid");
        spanMsg.appendChild(txtMsg);
        divInput.appendChild(label);
        divInput.appendChild(input);
        divInput.appendChild(spanMsg);
        form.appendChild(divInput);
    });
    if (title == 'Sign Up') {
        var divInput = document.createElement('div');
        divInput.setAttribute('class', 'form-floating mb-3');
        var name_1 = 'name';
        var input = document.createElement('input');
        input.setAttribute('class', 'form-control');
        input.setAttribute('type', "text");
        input.setAttribute('id', name_1);
        input.setAttribute('data-type', name_1);
        input.setAttribute('placeholder', name_1);
        input.setAttribute('minlength', "3");
        input.setAttribute('maxlength', "30");
        input.setAttribute('pattern', "^[a-zA-Z ]*$");
        var label = document.createElement('label');
        label.setAttribute('for', name_1);
        var txtLabel = document.createTextNode(name_1);
        label.appendChild(txtLabel);
        var spanMsg = document.createElement('span');
        spanMsg.setAttribute('class', 'input-message-error');
        var txtMsg = document.createTextNode("his field is not valid");
        spanMsg.appendChild(txtMsg);
        divInput.appendChild(label);
        divInput.appendChild(input);
        divInput.appendChild(spanMsg);
        form.appendChild(divInput);
    }
    var divButton = document.createElement('div');
    divButton.setAttribute('class', 'd-grid');
    var button = document.createElement('button');
    button.setAttribute('class', 'btn btn-lg btn-primary mt-3');
    var txtButton = document.createTextNode(title);
    button.appendChild(txtButton);
    divButton.appendChild(button);
    form.appendChild(divButton);
    container === null || container === void 0 ? void 0 : container.appendChild(form);
};
